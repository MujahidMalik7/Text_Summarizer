from transformers import pipeline

summarizer = pipeline("summarization", model="facebook/bart-large-cnn")

article = input("Enter your Paragraph to Summarize: ")

summary = summarizer(article, min_length = 60, max_length = 120, do_sample = False)

print("Summary : ") , summary[0].get('summary_text')

